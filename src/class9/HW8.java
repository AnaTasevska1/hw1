package class9;
// Create an array to store char values and then print those in reverse order
public class HW8 {
    public static void main(String[] args) {
        char[] initials={'A','N','C','E'};

        for (int i = 3; i >=0 ; i--) {
            System.out.print(initials[i]+" ");
        }
    }
}
